// ============================================
// API 6.13: SUPPLIER LIST WITH BUSINESS METRICS
// ============================================

export enum SupplierTier {
  TIER_1 = 'TIER_1',
  TIER_2 = 'TIER_2',
  TIER_3 = 'TIER_3',
}

export interface SupplierListDTO {
  supplierId: number;
  supplierCode: string;
  supplierName: string;
  phoneNumber: string;
  email?: string;
  address?: string;
  tierLevel?: SupplierTier;
  ratingScore?: number;
  totalOrders: number;
  lastOrderDate?: string; // YYYY-MM-DD
  isBlacklisted: boolean;
  isActive: boolean;
  notes?: string;
  createdAt: string; // ISO 8601
  updatedAt?: string; // ISO 8601
  
  // Helper method (calculated on FE)
  isInactive?: () => boolean;
}

export interface SupplierPageResponse {
  suppliers: SupplierListDTO[];
  currentPage: number;
  pageSize: number;
  totalElements: number;
  totalPages: number;
  isFirst: boolean;
  isLast: boolean;
  hasNext: boolean;
  hasPrevious: boolean;
}

